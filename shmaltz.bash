function shekel () {
	newline=${1}
	if [[ "${newline}" == "-n" ]]; then
		lineend=""
		shift
	elif [[ "${newline}" =~ ^- ]]; then
		echo "Not supported biotch"
		return 1
	else
		lineend="\n"
	fi

	printf "${@}${lineend}"
}

